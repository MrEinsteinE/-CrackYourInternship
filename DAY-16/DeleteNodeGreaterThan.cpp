class Solution
{
    public:
    Node *compute(Node *head)
    {
        Node* dummy = new Node(0);
        Node* curr = dummy;

        while(head != NULL){
            Node* temp = head;
            int maxi = head->data;
           while(temp != NULL){
                maxi = max(maxi, temp->data);
                if(maxi != head->data) break; 
                temp = temp->next;
            }
            if(maxi == head->data){
                Node* temp1 = new Node(head->data);
                curr->next = temp1;
                curr = curr->next;
            }
            
            head = head->next; 
        }

        curr->next = NULL; 
        return dummy->next; 
    }
    
};
   


